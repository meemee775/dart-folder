void main() {
//Declaring variables for numeric datatype(int)
  int price = 200;
  int tagnumber = 10;
  //print values
  print("price: $price and tagnumber: $tagnumber");

  //Declaring variables for numeric datatype(double)
  double weight = 6.2;
  double height = 1.5;
  //print values
  print('height :$height and weight :$weight');

//Declaring variables for String datatype
  String name = "John";
  String address = "Nigeria";
  //print values
  print("name :$name and address :$address");

//Declaring variables for List datatype
  List<String> statesinnigeria = ["Abia,Ebonyi,Enugu"];
  //print values
  print("statesinnigeria:$statesinnigeria");

  //Declaring variables for Map datatype
  Map<String, int> classaverage = {
    'Basic1': 70,
    'Basic2': 74,
    'Basic3': 76,
  };
  //print values
  print('classaverage: $classaverage');
}
